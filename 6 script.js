//exemplo for
for (let i = 0; i < 5; i++) {
  console.log("Número: " + i);
}
// Saída: 
// Número: 0
// Número: 1
// Número: 2
// Número: 3
// Número: 4



//exemplo while
let contador = 0;

while (contador < 5) {
  console.log("O contador é: " + contador);
  contador++; // Incremento crucial para evitar loop infinito
}
// Saída: 0, 1, 2, 3, 4